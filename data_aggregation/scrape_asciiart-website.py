for cat_url in tqdm(categories_urls):
    url = urllib.parse.urljoin(baseurl, cat_url)
    url = url.replace(" ", "%20")
    content = requests.get(url).content
    soup = BeautifulSoup(content, features="html.parser")
    copybuttons = soup.find_all(name="button", class_="copybutton")
    unique_count = 0
    for copy_button in copybuttons:
        art = copy_button.previous_sibling.text
        art = art.strip("\r\n")
        art = art.strip("\n")
        unique_count += 1
        filepath = "asciiart.website/{}/{}.txt".format(cat_url.removeprefix("index.php?art="), unique_count)
        filepath = os.path.join(output_dir, filepath)
        print("Saving ", filepath)
        os.makedirs(os.path.dirname(filepath), exist_ok=True)
        with open(filepath, "w") as f:
            f.write(art)
